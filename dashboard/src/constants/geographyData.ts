const getDeterministicHash = (value: string): number =>
  [...value].reduce((acc, char, index) => acc + char.charCodeAt(0) * (index + 1), 0);

const getJitter = (code: string, min: number, max: number): number => {
  const hash = getDeterministicHash(code);
  const normalized = (hash % 1000) / 1000;
  return min + normalized * (max - min);
};

const createWorldVisitors = (codes: readonly CountryCode[]): GeoVisitor[] => {
  const basePeak = 10500;
  const decay = 1.03;
  const minVisitors = 18;

  const visitors = codes.map((code, index) => {
    const base = basePeak / Math.pow(decay, index);
    const euBoost = EU_COUNTRIES.has(code) ? 1.18 : 1;
    const dkBoost = code === 'DK' ? 1.45 : 1;
    const jitter = getJitter(`${code}-${index}`, 0.85, 1.25);
    const visitorsCount = Math.round(Math.max(minVisitors, base * euBoost * dkBoost * jitter));

    return {
      country_code: code,
      visitors: visitorsCount,
    };
  });

  const maxVisitors = Math.max(...visitors.map((entry) => entry.visitors));
  const dkIndex = visitors.findIndex((entry) => entry.country_code === 'DK');
  if (dkIndex >= 0) {
    visitors[dkIndex] = {
      ...visitors[dkIndex],
      visitors: Math.max(visitors[dkIndex].visitors, Math.round(maxVisitors * 1.07)),
    };
  }

  return visitors;
};

const createCompareVisitors = (worldVisitors: GeoVisitor[]): GeoVisitor[] =>
  worldVisitors.map((entry, index) => {
    const swingJitter = getJitter(`${entry.country_code}-compare-${index}`, 0.9, 1.12);
    const direction = getDeterministicHash(`${entry.country_code}-dir`) % 2 === 0 ? 1 : -1;
    const factor = direction === 1 ? swingJitter : Math.max(0.82, 1 - (swingJitter - 1));
    const visitors = Math.round(Math.max(10, entry.visitors * factor));

    return {
      country_code: entry.country_code,
      visitors,
    };
  });

const WORLD_VISITORS = createWorldVisitors(WORLD_COUNTRY_CODES);

export const MOCK_WORLD_GEOVISITORS: GeoVisitor[] = WORLD_VISITORS;
export const MOCK_COMPARE_GEOVISITORS: GeoVisitor[] = createCompareVisitors(WORLD_VISITORS);
